def perimeter(a, b):
    '''
    Calculates the perimeter of a perimeter of a rectangle
    a, b (int): width and height
    returns perimeter of a rectangle (int)
    '''
    return 2 * (a + b)

def area(a, b):
    '''
    Calculates the area of a area of a rectangle
    a, b (int): width and height
    returns area of a rectangle (int)
    '''
    return a * b