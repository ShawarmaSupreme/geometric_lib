import math


def area(r):
    '''
    Calculate the area
    r (int): radius of the circle
    :return: area (int)
    '''
    return math.pi * r * r

def perimeter(r):
    '''
    Calculate the
    perimeter of a circle
    r (int): radius of the circle
    :return: perimeter (int)
    '''
    return 2 * math.pi * r

